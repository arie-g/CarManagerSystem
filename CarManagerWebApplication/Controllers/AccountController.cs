        [AllowAnonymous]
        public ActionResult ExternalLoginCallback(string returnUrl)
        {
            AuthenticationResult result = OAuthWebSecurity.VerifyAuthentication(Url.Action("ExternalLoginCallback", new { ReturnUrl = returnUrl }));
            if (!result.IsSuccessful)
            {
                return RedirectToAction("ExternalLoginFailure");
            }
            if (OAuthWebSecurity.Login(result.Provider, result.ProviderUserId, createPersistentCookie: false))
            {
                return RedirectToLocal(returnUrl);
            }

            if (User.Identity.IsAuthenticated)
            {
                // If the current user is logged in add the new account
                OAuthWebSecurity.CreateOrUpdateAccount(result.Provider, result.ProviderUserId, User.Identity.Name);
                if (result.Provider == "facebook" || result.Provider == "google")
                {
                    using (var db = new AuthDBEntities())
                    {
                        UserProfile user = db.UserProfiles.FirstOrDefault(u => u.UserName.ToLower() == User.Identity.Name);
                        if (user != null)
                        {
                            var oauthItem = db.webpages_OAuthMembership.FirstOrDefault(x => x.Provider == result.Provider && x.ProviderUserId == result.ProviderUserId && x.UserId == user.UserId);
                            if (oauthItem != null)
                            {
                                //oauthItem.Email = result.UserName;
                                db.SaveChanges();
                            }
                        }
                    }
                }
                return RedirectToLocal(returnUrl);
            }
            else
            {
                // User is new, ask for their desired membership name
                string loginData = OAuthWebSecurity.SerializeProviderUserId(result.Provider, result.ProviderUserId);
                ViewBag.ProviderDisplayName = OAuthWebSecurity.GetOAuthClientData(result.Provider).DisplayName;
                ViewBag.ReturnUrl = returnUrl;
                var model = new RegisterExternalLoginModel { UserName = result.UserName, ExternalLoginData = loginData };
                switch (result.Provider)
                {
                    case "facebook":
                    case "google":
                        {
                            model.Email = result.UserName;
                            model.UserName = "";
                            break;
                        }
                    case "twitter":
                        {
                            model.Email = "";
                            model.UserName = result.UserName;
                            break;
                        }
                    default:
                        break;

                }
                model.Companies = RoleService.GetCompanies();
                return View("ExternalLoginConfirmation", model);
            }
        }

        
        
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ExternalLoginConfirmation(RegisterExternalLoginModel model, string returnUrl)
        {
            string provider = null;
            string providerUserId = null;

            if (User.Identity.IsAuthenticated || !OAuthWebSecurity.TryDeserializeProviderUserId(model.ExternalLoginData, out provider, out providerUserId))
            {
                return RedirectToAction("Index","Home");
            }

            if (ModelState.IsValid)
            {
                // Insert a new user into the database
                using (var db = new AuthDBEntities())
                {
                    var user = db.UserProfiles.FirstOrDefault(u => u.UserName.ToLower() == model.UserName.ToLower());
                    // Check if user already exists
                    if (user == null && !String.IsNullOrEmpty(model.Phone))
                    {
                        // Insert name into the profile table
                        user = new UserProfile { UserName = model.UserName };
                        db.UserProfiles.Add(user);
                        db.SaveChanges();
                        var user1 = new Dal.User { Id = user.UserId, UserName = user.UserName };
                        db.Users.Add(user1);
                       await db.SaveChangesAsync();

                       var driver = new Dal.Driver()
                       {
                           Id = Guid.NewGuid(),
                           Name = model.UserName,
                           FamilyName = 
                           model.UserLastName,
                           Licence = model.Licence,
                           ExperienceYears = short.Parse(model.ExperienceYears),
                           UserId = user.UserId
                       };
                        List<Guid> companyTest = new List<Guid>();
                        companyTest.Add(new Guid("5556c0c6-0dcb-4ed3-a6cd-5aeb93cbd1fd"));
                       await RoleService.AddNewDriver(driver, companyTest);

                        OAuthWebSecurity.CreateOrUpdateAccount(provider, providerUserId, user.UserName);

                        OAuthWebSecurity.Login(provider, providerUserId, createPersistentCookie: false);

                        return RedirectToLocal(returnUrl);
                    }
                    else
                    {
                        if (String.IsNullOrEmpty(model.Phone))
                        {
